package com.fhd.icm.utils.mxgraph;

public class GraphCellPoint {
	private Double px;
	private Double py;
	private String pAs;
	public Double getPx() {
		return px;
	}
	public void setPx(Double px) {
		this.px = px;
	}
	public Double getPy() {
		return py;
	}
	public void setPy(Double py) {
		this.py = py;
	}
	public String getpAs() {
		return pAs;
	}
	public void setpAs(String pAs) {
		this.pAs = pAs;
	}
	
}
